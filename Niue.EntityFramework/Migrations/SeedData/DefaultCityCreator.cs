        private void AddCityIfNotExists(string name, string spellAll, string spellShort, string initial, string provinceId, string amapCityCode, string baiduCityCode, string longitude, string latitude)
        {
            var pId = Convert.ToInt32(provinceId);
            var province = _context.Cities.FirstOrDefault(c => c.Id == pId);

            if (province == null)
            {
                if (_context.Cities.Any(m => m.Name == name && m.Province == null))
                {
                    return;
                }
            }
            else
            {
                if (_context.Cities.Any(m => m.Name == name && m.Province != null && m.Province.Id == province.Id))
                {
                    return;
                }
            }

            var city = new City();
            if (province != null)
            {
                city.Province = province;
            }
            city.Name = name;
            city.SpellAll = spellAll;
            city.SpellShort = spellShort;
            city.Initial = initial;
            city.AmapCityCode = amapCityCode;
            city.BaiduCityCode = baiduCityCode;
            city.Longitude = longitude;
            city.Latitude = latitude;
            city.SchoolSecurityCode = 1;
            _context.Cities.Add(city);
            _context.SaveChanges();
        }
